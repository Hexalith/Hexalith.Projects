    [Fact]
    public void CanonicalHasherIsDeterministic()
    {
        IdempotencyField[] fields =
        [
            new IdempotencyField("a_field", true, "alpha"),
            new IdempotencyField("b_field", true, 42),
        ];

        HexalithIdempotencyHasher.Compute("Op", fields)
            .ShouldBe(HexalithIdempotencyHasher.Compute("Op", fields));
        HexalithIdempotencyHasher.Compute("Op", fields).ShouldStartWith("sha256:");
    }

    [Fact]
    public void CanonicalHasherEnforcesDeclaredOrder()
    {
        Should.Throw<InvalidOperationException>(() => HexalithIdempotencyHasher.Compute("Op", [
            new IdempotencyField("z_field", true, "last"),
            new IdempotencyField("a_field", true, "first"),
        ]));
    }

    [Fact]
    public void CanonicalHasherRejectsDuplicateField()
    {
        Should.Throw<InvalidOperationException>(() => HexalithIdempotencyHasher.Compute("Op", [
            new IdempotencyField("a_field", true, "first"),
            new IdempotencyField("a_field", true, "duplicate"),
        ]));
    }

    [Fact]
    public void CanonicalHasherEnforcesUtcDates()
    {
        Should.Throw<InvalidOperationException>(() => HexalithIdempotencyHasher.Compute("Op", [
            new IdempotencyField("when", true, DateTime.SpecifyKind(DateTime.UnixEpoch, DateTimeKind.Unspecified)),
        ]));

        // UTC is accepted.
        HexalithIdempotencyHasher.Compute("Op", [
            new IdempotencyField("when", true, DateTime.SpecifyKind(DateTime.UnixEpoch, DateTimeKind.Utc)),
        ]).ShouldStartWith("sha256:");
    }

    [Fact]
    public void CanonicalHasherRejectsControlCharacters()
    {
        // The canonical hasher rejects control characters in the operationId and in declared field
        // paths (the cross-surface idempotency contract — matches the Folders sibling). Control
        // characters inside string *values* are escaped, not rejected, so these assertions target the
        // operation and path surfaces where rejection is the contract.
        Should.Throw<InvalidOperationException>(() => HexalithIdempotencyHasher.Compute("Op\u0001", [
            new IdempotencyField("a_field", true, "value"),
        ]));

        Should.Throw<InvalidOperationException>(() => HexalithIdempotencyHasher.Compute("Op", [
            new IdempotencyField("bad\u0001field", true, "value"),
        ]));
    }

    [Fact]
    public void CanonicalHasherDistinguishesPresentFromOmitted()
    {
        string present = HexalithIdempotencyHasher.Compute("Op", [new IdempotencyField("v", true, null)]);
        string omitted = HexalithIdempotencyHasher.Compute("Op", [new IdempotencyField("v", false, null)]);
        present.ShouldNotBe(omitted);
    }

    private static void AssertGeneratedArtifactIsSafe(string generated)
    {
        generated.ShouldNotContain(RepositoryRoot, Case.Insensitive);
        generated.ShouldNotContain("https://", Case.Insensitive);
        generated.ShouldNotContain("provider_token", Case.Insensitive);
        generated.ShouldNotContain("credential_material", Case.Insensitive);
        generated.ShouldNotContain("-----BEGIN", Case.Insensitive);
    }

    private static string ExpectedHash(params string[] lines)
    {
        string canonical = string.Join('\n', lines);
        byte[] digest = SHA256.HashData(Encoding.UTF8.GetBytes(canonical));
        return "sha256:" + Convert.ToHexString(digest).ToLowerInvariant();
    }

    private static string LocateRepositoryRoot()
    {
        string assemblyLocation = typeof(ClientGenerationTests).Assembly.Location;
        string startDirectoryPath = string.IsNullOrEmpty(assemblyLocation)
            ? AppContext.BaseDirectory
            : (Path.GetDirectoryName(assemblyLocation) ?? AppContext.BaseDirectory);

        DirectoryInfo? directory = new(startDirectoryPath);
        while (directory is not null)
        {
            if (File.Exists(Path.Combine(directory.FullName, "Hexalith.Projects.slnx")))
            {
                return directory.FullName;
            }

            directory = directory.Parent;
        }

        throw new InvalidOperationException("Could not locate Hexalith.Projects.slnx from the test assembly location.");
    }
