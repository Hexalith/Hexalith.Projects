    [Fact]
    public void FingerprintDetectsDriftInEachInput()
    {
        string spine = File.ReadAllText(Path.Combine(RepositoryRoot, "src", "Hexalith.Projects.Contracts", "openapi", "hexalith.projects.v1.yaml"));
        string configuration = File.ReadAllText(Path.Combine(RepositoryRoot, "src", "Hexalith.Projects.Client", "nswag.json"));
        string helpers = File.ReadAllText(Path.Combine(RepositoryRoot, "src", "Hexalith.Projects.Client", "Generated", "HexalithProjectsIdempotencyHelpers.g.cs"));

        // Baseline: the unmutated triple is current.
        HexalithProjectsGeneratedArtifacts.IsCurrent(spine, configuration, helpers).ShouldBeTrue();

        // Mutating any single input is detected as drift.
        HexalithProjectsGeneratedArtifacts.IsCurrent(spine + "\n# controlled stale-output test", configuration, helpers).ShouldBeFalse();
        HexalithProjectsGeneratedArtifacts.IsCurrent(spine, configuration.Replace("LF", "CRLF", StringComparison.Ordinal), helpers).ShouldBeFalse();
        HexalithProjectsGeneratedArtifacts.IsCurrent(spine, configuration, helpers + "\n// controlled stale-output test").ShouldBeFalse();
    }

